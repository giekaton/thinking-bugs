      <h4>Explained</h4>

      <p>Our mind does this error when we look at things from two opposite perspectives and think that it’s either one or the other, without anything in between.</p>

      <p>A person engaging in this type of thinking sees things in a binary mode. Everything becomes either 0 or 1, On or Off, Good or Bad.</p>

      <p>Also called "Black and White" thinking or "Dichotomous" thinking, this error makes us to evaluate life in extreme terms: It's either perfect or a disaster.</p>

      <p>When used intentionally, a little exaggeration might not be damaging, and can even increase creativity, motivation or give other benefits. All or nothing thinking is harmful when it appears as an automatic mind pattern and makes us see the world through the black and white lens.</p>

      <p>When all or nothing thinking error works together with other thinking errors, they amplify each other and create chain reactions.</p>

      <p>With All or Nothing + Jumping to Conclusions and the Mental Filter distortions, it is not difficult to dive straight into the deepest form of depression.</p>

      <p>It is a thinking error because it oversimplifies and distorts reality. In most situations, the answer is never just a binary 1 or 2. The reality is much more nuanced, and the truth is somewhere in between.</p>

      <p>The "black and white" worldview is subjective to every individual. When other people, not engaged in all or nothing thinking, observe the same situations, they do not make the same self-defeating conclusions, as the individual, doing the error.</p>

