<div class="site-content"><div><h1><span>Other Blame</span></h1> <div>
<h3>You blame others and overlook ways you contributed to the problem. </h3>



<h4>Description</h4>



<p>It is tempting to blame other people or some external situations when things don’t go the way we want.</p>



<p>When the Other Blame is in action, a person blames someone else or some external factors for failures he or she is experiencing. It can also be just a habit of looking for faults in others, ignoring personal responsibility and contribution to the situation.</p>



<p>Sometimes this error can be used intentionally as a competitive strategy, though the produced results are often short-lived and have negative consequences that follow.</p>



<p>This cognitive distortion can be seen not only on the individual but also on the global scale, when nations are blaming each other, engaging in endless wars. In such situations, each side feels completely right. For them, it’s always the other side which is wrong.</p>



<h4>Example</h4>



<p>The thinking error of Other Blame is when:<br>– The individual responsibility is ignored, and the other is blamed inadequately.<br>– Other people or situations are blamed in general, as a habit.</p>



<p>When it’s negative and on a personal level, the Other Blame cognitive distortion can create a situation, where a person thinks that everyone is against him and are sabotaging his or her endeavors. Not only it isolates, but also creates a negative worldview, which contributes to bad mood and the lack of energy.</p>



<h4>Why is this a thinking error?</h4>



<p>The reality is much more complex than a simple Yes or No. The truth is never absolute, and neither reality nor people are obliged to act the way we want them to.</p>



<h4>How to fix it?</h4>



<p>Write the distorted thought in the <a href="https://thinkingerrors.org/cbt-app" target="_blank" rel="noreferrer noopener" aria-label="CBT journal (opens in a new tab)">CBT journal</a>, and then challenge it. Try to understand the reasons and motivations behind situations and actions. Rewrite the negative thought in a realistic and positive way.</p>



<p>The thinking error of the Other Blame is often associated with an individual’s ego and self-worth. That is why it can be hard to challenge this error. But left unchallenged, it creates anger and escalates the conflict, or results in negative mood and depression.</p>
