      <h4>Explained</h4>

      <p>
        The thinking error of Other Blame is when:
        <br />– The individual responsibility is ignored, and someone else is blamed inadequately.
        <br />– Other people or situations are blamed in general, as a habit.
      </p>

      <p>It is tempting to blame other people or some external situations when things don’t go the way we want.</p>

      <p>When the Other Blame is in action, a person blames someone else or some external factors for failures he or she is experiencing. It can also be just a habit of looking for faults in others, ignoring personal responsibility and contribution to the situation.</p>

      <p>
        When it is negative and on a personal level, the Other Blame cognitive distortion can create a situation where a person thinks that people are sabotaging his or her endeavors. Not only it isolates, but also creates a negative worldview, which contributes to bad mood and the lack of energy.
      </p>

      <p>Sometimes this error can be used intentionally as a competitive strategy, though the produced results are often short-lived and have negative consequences that follow.</p>

      <p>This cognitive distortion can be seen not only on the individual but also on the global scale, when nations are blaming each other, engaging in endless wars. In such situations, each side feels completely right. For them, it’s always the other side which is wrong.</p>

      <p>The reality is much more complex than a simple Yes or No. The truth is never absolute, and neither reality nor people are obliged to act the way we want them to.</p>

