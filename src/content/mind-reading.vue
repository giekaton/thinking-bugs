      <h3>You assume that people are reacting negatively to you.</h3>

      <h4>
        <strong>Example</strong>
      </h4>

      <p>Alice looked at Bob in a strange way. Bob concluded that Alice doesn’t like his appearance and has no good feelings for him. Actually, at that particular moment, Alice was thinking when, if ever, Bob is going to invite her to a date.</p>

      <h4>Explained</h4>

      <p>Mind Reading is an irrational belief that we know what the other person is thinking.</p>

      <p>This sometimes can be accurate because we can interpret body language, or maybe we know the other person so well that we can guess quite accurately what she or he is thinking. Even in such cases, at best, that would be only a well-informed guess, but never the true reality because by guessing, we can never know for sure.</p>

      <p>When it’s a harmful thinking error, the Mind Reading is done automatically without us being aware of it. Most often it results in negative conclusions, but it also has its irrationally positive form.</p>

      <p>The person who is suffering from this thinking error the most is doing the biggest amount of uninformed and automatic guesses, while at the same time blindly believing them all to be true.</p>

      <p>Unless it is a movie or a sci-fi novel, people cannot read other people's minds. That's a commonly known truth.</p>


      <h4>
        <strong>How to fix it</strong>
      </h4>

      <p>
        You can fix your thinking bugs with the help of the classical Cognitive Behavioral Therapy. Here, it is presented in a digital format, as the <a href="https://thinkingbugs.com/cbt-app" target="_blank">CBT app</a>.
      </p>

      <p>
        Record the negative thought in the CBT app. Then identify the Mind Reading thinking bug. Finally, rewrite the thought more realistically, considering other possible interpretations of the situation.
      </p>


