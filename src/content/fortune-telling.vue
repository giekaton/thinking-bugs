      <h3>You jump to conclusions not warranted by the facts.</h3>

      <h4>Example</h4>

      <p>Bob is convinced that the relationship with Alice will have a bad end, therefore he is already preparing for it, and has an overall negative outlook on the relationship.</p>

      <h4>Explained</h4>

      <p>As the name of this cognitive distortion suggests, fortune telling is when we try to predict the future and believe our predictions as if they are true.</p>

      <p>We make this thinking error when our mind creates a generalized conclusion about some future situation. The conclusion is often irrational and unrealistic.</p>

      <p>This thinking error is in action when the mind cannot tolerate the unknown. It rushes to the conclusion and fills the empty space with any answer it can find.</p>

      <p>For many people, it is often easier to accept even the negative conclusion than to tolerate the unknown in which both positive and negative outcomes are possible.</p>

      <p>When a person makes a negative conclusion, thoughts about failure can result in real-life failure because what we think about often gets energized and manifests in real life.</p>

      <p>Fortune Telling (also called Jumping to Conclusions) results in a self-inflicted defeat when it’s negative, and when positive – in unrealistic illusions and over-confidence.</p>

      <p>It is a well-known fact in psychology and general science that people cannot predict the future.</p>

      <p>People try predicting things professionally (actuaries, investment advisors, researchers, sci-fi authors), but that’s not what we are talking about.</p>

      <p>When we are talking about the thinking error of Fortune Telling, the prediction is not based on data, it is often subjective, made on an individual level. Most of the time the individual is not even aware that he or she made the prediction and is doing the Fortune Telling error. It is an irrational belief and an inaccurate distortion of reality.</p>

