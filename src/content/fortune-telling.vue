    <div>
      <h3>You jump to conclusions not warranted by the facts.</h3>

      <h4>Example</h4>

      <p>Bob is convinced that the relationship with Alice will have a bad ending, therefore he is already preparing for it, and has an overall negative outlook on the relationship.</p>

      <h4>Explained</h4>

      <p>As the name of this cognitive distortion suggests, the Fortune Telling bug happens when we try to predict the future and believe our predictions as if they are true, automatically and uncritically.</p>

      <p>We make this error when our thinking creates a generalized conclusion about some future situation. The conclusion is often irrational, unrealistic, or simply not as good as it could be.</p>

      <p>Sometimes this happens when we cannot tolerate the unknown. We rush to the conclusion and try to fill the empty space with any answer we can find.</p>

      <p>For many people, it is often easier to accept even the negative conclusion than to tolerate the unknown, in which both positive and negative outcomes are possible.</p>

      <p>When a person makes a negative conclusion, thoughts about failure can result in real-life failure because what we think about, can get energized and become a reality.</p>

      <p>Fortune Telling (also called Jumping to Conclusions) results in a self-inflicted defeat when it’s negative, and when irrationally positive – in unrealistic illusions and over-confidence.</p>

      <p>It is a well-known fact in psychology and general science that people cannot predict the future.</p>

      <p>People try predicting things professionally (actuaries, investment advisors, researchers, sci-fi authors), but that’s a structured approach, based on data, so it is not a thinking bug.</p>

      <p>When we are talking about the thinking error of the Fortune Telling, the prediction is not based on data. It is often subjective, made on an individual level, automatically. Most of the time, the individual is not even aware that he or she made the prediction and is doing the Fortune Telling error.</p>

      <h4>How to fix it</h4>

      <p>
        When using the
        <a href="https://thinkingbugs.com/cbt-app" target="_blank">CBT app</a>, inspect your thought. Are your future predictions based on data, do they have rational foundations, are they true?
      </p>

      <p>Metaphorically speaking, do not allow your mind to take such gigantic leaps of faith (straight to the all-encompassing conclusion), especially when there is no rational basis.</p>

      <p>If not sure, ask a friend or consult with a CBT specialist.</p>

