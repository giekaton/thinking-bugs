    <h3>You dwell on the negatives and ignore the positives.</h3>

    <h4>Example</h4>

    <p>Alice was in her garden, taking care of plants. She was working there from early morning, feeling great and did a lot of good work that day, even more than planned. Then she accidentally stepped on a newly planted tomato. Alice got an instant inner reaction blaming herself how could she do this. Even though a lot of good work was done that day, the whole next week she was ruminating about the tomato plant she stepped upon. Even some years later, she was still recalling that event and feeling "down" during these moments.</p>

    <h4>Explained</h4>

    <p>The Mental Filter is a distortion of reality through a tendency to interpret things negatively.</p>

    <p>A person doing this error is not seeing the positive aspects of the situation and instead is concentrating only on the negatives.</p>

    <p>
      When the Mental Filter is in action, it is also accompanied by the phenomenon of "selective attention". It narrows our worldview, and focuses our attention on the negative things or events, or makes us interpret neutral things as negative.
    </p>

    <p>For some people, mental filtering is a character quality, linked to the person’s identity. After practicing CBT journaling technique with different situations, the thinking error of mental filtering can be changed to a more healthy and positive worldview.</p>

    <p>Cognitive distortions can be both negative and positive. When the Mental Filter is irrationally positive, it creates inadequately positive expectations. Because they are unrealistic, they finally conclude to negative results. The metaphor for this is “seeing things through the rose-tinted glasses”.</p>

    <p>The Mental Filter creates an irrational worldview that is not adequate or realistic because it goes through an artificial mental tendency to shape things in one way or another.</p>

    <p>In some cases, this can work and can be practiced intentionally. For example, there is the phrase “fake it until you make it”. But of course, for a person who is suffering from the automatic mental filtering, first there is a need to regain freedom from it. Then it may be used in new creative and practically applicable ways.</p>

    <p>
      On the Thinking Bugs website you can often find the term "rationally / practically / realistically positive", which in other words can be expressed as "realistic optimism". Such optimism is also a slight positive Mental Filter. But here, there are many good arguments why the mental distortion of optimism is considered normal and even preferable - mostly because optimistic mindset can benefit our lifes on many different levels.
    </p>

