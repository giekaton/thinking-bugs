    <h3>Thinking patterns that ignore causal relationships of reality.</h3>

    <p>Magical Thinking is a group of unrealistic thinking patterns some people use to explain the world. The common similarity of this type of thinking is that the explanation ignores the actual cause and effect of reality, as it is known by science and logic, and can be observed by other people who are not engaged in Magical Thinking.</p>

    <p>Magical Thinking is a belief that one event happens as a result of another without any plausible link of causation, or to put it differently: it's believing in things more strongly than either evidence or experience justifies.</p>
  
    <p>Many of us engage in Magical Thinking, and such activity is not always harmful. Magical Thinking is normal in children, or it can be even desirable in professional creative people.</p>

    <p>Magical Thinking becomes harmful, when this thinking bug starts to control us, lowers our quality of life, prevents us from reaching our potential, or causes other similar harm. Below are presented examples of potentially harmful Magical Thinking.</p>

    <h4>Over-Worrying and Over-Thinking</h4>

    <p>Many anxious people tend to believe that constantly worrying about something can prevent a catastrophe.</p>

    <i>"The patient has the superstitious belief that the anxiety, although uncomfortable, protects him or her from some terrible catastrophe." – Dr. David D. Burns</i>

    <p>The person believes that if he or she is not constantly worrying about something, bad things will happen. Being stuck in such Magical Thinking, the individual self-sabotages his or her own well-being by believing that anxiety is a form of protection. Such behavior is like a ritual or a bad habit, which creates a temporary relief but is harmful in the long-run.</p>

    <p>Anxiety can be beneficial when we know how to use it and not being in the worrying mode all the time. When worrying constantly, such activity consumes our mental energy, weakens the mind, and then it may fail to perceive real dangers, because being constantly busy in its internal monologue, the mind has no time and attention left for real-life situations.</p>

    <p>To benefit from anxiety, instead of worrying all the time on auto-pilot, we need to listen to the clues that our anxiety presents, and then plan ahead constructively. For example, if you are going to have a complicated trip, and you are worrying that there will be a lot of stress or some bad things will happen, a reasonable step would be to plan ahead everything and prepare yourself for the trip as best as possible, instead of worrying all the time about the unknowns but actually not doing anything.</p>


