  <h3>You blow things way out of proportion or shrink them. </h3>



  <p>Magnification or minimization is a highly inaccurate inner reaction to an outside object or event.</p>



  <p>People tend to magnify their failures. They keep thinking about the same thing, and by spending a lot of time thinking about it, they magnify it. Something, that could have been easily ignored at the beginning, sticks and grows, and becomes a real thing – a self-fulfilling prophecy.</p>



  <p>People also tend to minify their good qualities, achievements and other positive things about life. A meaningful personal achievement goes unnoticed (minification), while a minor failure becomes an all-consuming object of worry (magnification).</p>



  <p>When you notice this thinking error in your thought, make a little pause, change a perspective, or ask someone’s opinion. In what ways are you magnifying or minifying things? Use the <a href="https://thinkingerrors.org/cbt-app" target="_blank" rel="noreferrer noopener" aria-label="CBT app (opens in a new tab)">CBT app</a> to re-write your thought in a more positive and realistic way.</p>
