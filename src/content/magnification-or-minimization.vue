        <h4>Explained</h4>

        <p>Magnification or Minimization is an inadequate reaction to an outside object or event.</p>

        <p>It is a well-known fact that many people magnify their failures. They keep thinking about the same thing again and again, and make a big deal out of it.  Something, that could have been easily ignored at the beginning, sticks and grows, and becomes a real thing – a self-fulfilling prophecy. In other words, magnification is a dramatization, and it is a result of over-thinking.</p>

        <p>Thinking is a great instrument, but when left on auto-pilot, in many cases it creates more harm than value.</p>

        <p>People also tend to minify their good qualities, achievements and other positive things about life. A meaningful personal achievement goes unnoticed (minification), while a minor failure becomes an all-consuming object of worry (magnification).</p>
        
        <p>This thinking bug is closely linked with the Loss-Aversion cognitive distortion (not part of CBT), which is a tendency to prefer avoiding losses to acquiring equivalent gains: "it is better to not lose $10 than to find $10".</p>

        <p>When a person is deeply involved in his or her thinking, it can be very hard to step aside and take a fresh look at the situation. It is good to keep in mind that thoughts are just an interpretation of reality, not the actual reality.</p>

        <p>Catastrophizing is a singe sub-type of this cognitive distortion. It appears when we give greater weight to the worst possible outcome, when the actual chances of it happening are very low, e.g. "The plane didn’t land at the expected time. What if there’s been an accident?".</p>


