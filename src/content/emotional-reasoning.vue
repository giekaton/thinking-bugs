      <h3>You reason from your feelings: “I feel like an idiot, so I must be one.”</h3>

      <h4>Example</h4>

      <p>After a hard day at work, Bob came home and got into a conflict with his children. There was no rational reason for the conflict. Bob was just feeling bad after the hard day at work and wanted to do something about it.</p>

      <h4>Explained</h4>

      <p>This is a very unique thinking error, and an interesting phenomenon to explore. It connects the feeling with the thought.</p>

      <p>A person feels bad, and as a consequence thinks bad. This thinking then amplifies the feeling and creates a loop.</p>

      <p>The thought process is similar to this: “I feel bad, therefore there should be something wrong with me”.</p>

      <p>When we start looking for negatives, we can always find them.</p>
      
      <p>Stuck in the Emotional Reasoning, we start searching for negatives in the form of a question “what’s wrong with me?”.</p>

      <p>Emotional negativity can be felt because of many reasons, some of them can be hard to explain, others – an indirect sign of progress, or arising from some natural life challenges.</p>
      
      <p>Emotional suffering can also be legitimate - an adequate response to some life situation. In this case, it is important to accept it.</p>

      <p>When it's an error, it can be fixed, and as a consequence - life conditions improved.</p>

      <p>
        The emotional discomfort creates the urge to act. Such action is often unnecessary or harmful (or both), especially when it arises not from the situation at hand but from some internal conflict, or previous traumatic experiences.
      </p>

      <p>
        Emotional Reasoning can also be felt indirectly as physical body symptoms, aches and pains that have no diagnosable cause. For similar cases, some specialists suggest people to ignore (through acceptance) the symptoms all together because their root causes lie in the persons thinking (mind and body relationship). Some great books on this subject are written by
